#Four examples where you can use for loop

#1
b = 10
chapters = range(1,11)
for b in chapters:
    print(f'Read book')


#2
C = "chelsea"
teams = "barca", "madrid", "chelsea"
for C in teams:
    print(f'Start competition')

#3
S = "Shoe"
outfit = "Shirt", "Trouser","Shoe","Cap"
for S in outfit:
    print(f'You can enter the party')

#4
Slt = "Salt"
ingredients = "Salt", "Onions", "crayfish"
for Slt in ingredients:
    print(f'Cook food')